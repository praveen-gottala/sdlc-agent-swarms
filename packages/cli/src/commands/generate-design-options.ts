/** Build the user prompt with app context. */
function buildUserPrompt(context: {
  appName: string;
  description: string;
  targetAudience: string;
  prdContent?: string;
}): string {
  const appContext = context.prdContent
    ? `- App name: ${context.appName}\n\nPRD:\n${context.prdContent}`
    : `- App name: ${context.appName}\n- Description: ${context.description || 'A web application'}\n- Target audience: ${context.targetAudience || 'general'}`;

  return `Generate 3 design system options for:\n${appContext}`;
}

/** Parse and validate LLM response into DesignOption array. */
export function parseLLMResponse(raw: string): DesignOption[] {
  // Strip markdown code fences if present
  const cleaned = raw.replace(/^```(?:json)?\s*/m, '').replace(/\s*```\s*$/m, '').trim();
  const parsed = JSON.parse(cleaned) as { options: DesignOption[] };

  if (!Array.isArray(parsed.options)) {
    throw new Error('Response missing "options" array');
  }

  return parsed.options.filter((opt) => {
    if (!opt.label || !opt.vibe) return false;
    if (!opt.colors?.primitive || !opt.colors?.semantic) return false;
    if (Object.keys(opt.colors.primitive).length < 5) return false;
    if (!opt.colors.semantic['background-primary'] || !opt.colors.semantic['text-primary']) return false;
    if (!opt.colors.semantic['cta-primary'] || !opt.colors.semantic.error) return false;
    if (!opt.fonts?.display || !opt.fonts?.body) return false;
    if (!opt.brand?.tone) return false;
    return true;
  });
}

/** Convert a DesignOption to DesignTokensSpec. */
export function optionToTokens(option: DesignOption): DesignTokensSpec {
  return {
    version: '1.0',
    created_by: 'agentforge-init-llm',
    colors: {
      primitive: option.colors.primitive,
      semantic: option.colors.semantic,
    },
    typography: {
      font_families: { display: option.fonts.display, body: option.fonts.body },
      scale: [...FIXED_TYPOGRAPHY_SCALE],
    },
    spacing: SHARED_LAYOUT.spacing,
    borders: SHARED_LAYOUT.borders,
    touch_targets: SHARED_LAYOUT.touch_targets,
    ...(option.components ? { components: option.components } : {}),
  };
}

/** Convert a DesignOption to BrandSpec. */
export function optionToBrand(option: DesignOption, audience: string): BrandSpec {
  return {
    version: '1.0',
    created_by: 'agentforge-init-llm',
    identity: {
      tone: option.brand.tone,
      audience: audience || 'general',
    },
    illustration_style: {
      direction: option.brand.illustrationDirection || 'minimal',
      description: option.brand.illustrationDescription || 'Clean illustrations with accent color highlights',
    },
    motion_principles: {
      page_transitions: 'fade',
      interaction_feel: option.brand.motionFeel || 'snappy',
      easing: 'ease-out',
      duration_base_ms: 200,
    },
    accessibility: {
      wcag_level: 'AA',
    },
  };
}

/** Build the 3 fallback options from existing archetypes. */
function buildFallbackOptions(): DesignOption[] {
  const archetypes: { key: DesignArchetype; label: string; vibe: string }[] = [
    { key: 'warm', label: 'Warm & Inviting', vibe: 'Cream, teal, rounded, friendly — approachable and playful' },
    { key: 'professional', label: 'Clean & Professional', vibe: 'White, slate, blue accent — structured and trustworthy' },
    { key: 'bold', label: 'Bold & Modern', vibe: 'Dark, violet, lime — geometric and cutting-edge' },
  ];

  return archetypes.map(({ key, label, vibe }) => {
    const tokens = buildDesignTokensSpec(key);
    const semantic = tokens.colors.semantic as unknown as DesignOption['colors']['semantic'];
    return {
      label,
      vibe,
      colors: { primitive: tokens.colors.primitive as Record<string, string>, semantic },
      fonts: tokens.typography.font_families as { display: string; body: string },
      brand: {
        tone: key === 'warm' ? 'playful-warm' : key === 'professional' ? 'professional-clean' : 'bold-modern',
        illustrationDirection: 'minimal',
        illustrationDescription: 'Clean illustrations with accent color highlights',
        motionFeel: 'snappy',
      },
    };
  });
}

/** Resolve a semantic color to its hex value. */
function resolveColor(semantic: string, primitive: Record<string, string>): string {
  // If semantic value starts with #, it's already a hex
  if (semantic.startsWith('#')) return semantic;
  return primitive[semantic] ?? '#888888';
}

