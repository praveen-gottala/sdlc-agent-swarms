/** Attempt LLM generation of the app spec. */
async function tryLLMGeneration(
  apiKey: string,
  context: {
    readonly appName: string;
    readonly description: string;
    readonly tokens: DesignTokensSpec;
    readonly brand: BrandSpec;
    readonly prdContent?: string;
  },
  output: NodeJS.WritableStream,
): Promise<GeneratedAppSpec | null> {
  let provider: LLMProvider;
  try {
    provider = createClaudeProvider('claude-sonnet-4', { apiKey });
  } catch {
    output.write(warnMsg('Failed to create LLM provider.\n'));
    return null;
  }

  const systemPrompt = buildSystemPrompt();
  const userPrompt = buildUserPrompt(context);

  for (let attempt = 0; attempt < 2; attempt++) {
    try {
      const result = await provider.complete(
        {
          system: systemPrompt,
          messages: [
            { role: 'user', content: userPrompt },
            ...(attempt > 0
              ? [{ role: 'user' as const, content: 'Your previous response was not valid JSON. Please respond with ONLY the JSON object, no markdown.' }]
              : []),
          ],
        },
        { model: 'claude-sonnet-4', maxTokens: 8192, temperature: 0.7 },
      );

      if (!result.ok) {
        output.write(warnMsg(`LLM request failed: ${JSON.stringify(result.error)}\n`));
        return null;
      }

      const spec = parseAppSpecResponse(result.value.content);
      if (!spec) {
        if (attempt === 0) continue;
        output.write(warnMsg('LLM returned invalid spec structure.\n'));
        return null;
      }

      return spec;
    } catch (e) {
      if (attempt === 0) continue;
      output.write(warnMsg(`LLM generation error: ${e instanceof Error ? e.message : String(e)}\n`));
      return null;
    }
  }

  return null;
}

