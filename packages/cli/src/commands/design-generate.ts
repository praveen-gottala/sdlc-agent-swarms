/** Check if a hex color is light. */
function isLight(hex: string): boolean {
  const c = hex.replace('#', '');
  const r = parseInt(c.substring(0, 2), 16);
  const g = parseInt(c.substring(2, 4), 16);
  const b = parseInt(c.substring(4, 6), 16);
  return (r * 299 + g * 587 + b * 114) / 1000 > 128;
}

/** Resolve a semantic color to its hex value. */
function resolveColor(semantic: string, primitive: Readonly<Record<string, string>>): string {
  if (semantic.startsWith('#')) return semantic;
  return primitive[semantic] ?? '#888888';
}

