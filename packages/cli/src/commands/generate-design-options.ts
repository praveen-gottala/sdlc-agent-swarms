/**
 * Convert structured output to DesignOption format.
 * Handles the core schema fields + parses the "extras" JSON string for
 * optional tokens (typography_scale, borders, motion, preview).
 */
function normalizeStructuredOptions(
  raw: { options: Array<Record<string, unknown>> },
): DesignOption[] {
  return raw.options.map((opt) => {
    const colors = opt.colors as { primitive: Array<{ name: string; hex: string }> | Record<string, string>; semantic: Record<string, string> };
    // Convert primitive array → Record if the structured schema was used
    const primitive: Record<string, string> = Array.isArray(colors.primitive)
      ? Object.fromEntries(colors.primitive.map((c) => [c.name, c.hex]))
      : colors.primitive;

    const result: Record<string, unknown> = {
      ...opt,
      colors: { primitive, semantic: colors.semantic },
    };

    // Parse extras JSON string for optional tokens
    const extrasRaw = opt.extras as string | undefined;
    if (extrasRaw && typeof extrasRaw === 'string') {
      try {
        const extras = JSON.parse(extrasRaw) as Record<string, unknown>;

        // Typography scale: coerce size/weight/line_height from "24px" → 24
        if (extras.typography_scale && Array.isArray(extras.typography_scale)) {
          result.typography_scale = (extras.typography_scale as Record<string, unknown>[]).map((entry) => ({
            ...entry,
            size: parseFloat(String(entry.size)) || 14,
            weight: parseFloat(String(entry.weight)) || 400,
            line_height: parseFloat(String(entry.line_height)) || 1.5,
          }));
        }

        if (extras.borders) result.borders = extras.borders;

        // Motion: coerce durations from "150ms" → 150
        if (extras.motion) {
          const motion = extras.motion as Record<string, unknown>;
          const durations = motion.durations as Record<string, unknown> | undefined;
          if (durations) {
            motion.durations = {
              fast: parseFloat(String(durations.fast)) || 100,
              normal: parseFloat(String(durations.normal)) || 200,
              slow: parseFloat(String(durations.slow)) || 400,
            };
          }
          result.motion = motion;
        }

        if (extras.border_styles) result.border_styles = extras.border_styles;
        if (extras.text_extras) result.text_extras = extras.text_extras;

        if (extras.preview) result.preview = extras.preview;
      } catch {
        // Malformed extras — skip, non-critical
      }
    }
    delete result.extras;

    return result as unknown as DesignOption;
  });
}

/** Build the system prompt for the LLM. */
function buildSystemPrompt(output: NodeJS.WritableStream): string {
  const systemPrompt = `You are a design system expert. Generate 3 distinct design direction options for a web application.

Each option should feel meaningfully different — vary the mood, color temperature, and typography personality.

Rules:
- 5-8 primitive colors per option, using kebab-case names (e.g. "deep-teal", "warm-cream")
- Semantic color values should reference primitive color names (except overlay which is an rgba value)
- Use real Google Fonts that pair well
- Ensure sufficient contrast between background-primary and text-primary (WCAG AA)
- surface-secondary should be a subtle variant of surface-primary (e.g. slightly darker or lighter) for nested content areas
- surface-input should be the background for input fields (often same as background-primary or slightly different)
- Elevation shadows should feel cohesive with the design direction (4 levels: flat, cards, dropdowns, modals)

The "extras" field is a JSON string for optional design tokens. Include a JSON object with any of these keys:
- "typography_scale": array of {role, size, weight, family, line_height} — include if design needs non-standard sizing. 6 roles: heading-1, heading-2, heading-3, body, label, small. "family" must be "display" or "body" (abstract keys, not actual font names).
- "borders": {radius: {small, medium, large, pill}} — include for specific corner treatment (small=0 for brutalist, large=24 for soft/playful).
- "motion": {durations: {fast, normal, slow}, easings: {default, emphasized}} — include if brand personality suggests specific animation character.
- "preview": {metrics: [{label, value, trend}], table_rows: [{name, status, amount, date}], nav_items: [string]} — SHOULD include 3 metrics, 4 table_rows, 4 nav_items matching the app domain.`
  debugLog('buildSystemPrompt: Building system prompt');
  return systemPrompt;
}

