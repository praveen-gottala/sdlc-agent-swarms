function loadSpec(name: string): DesignSpecV2 | null {
  const specPath = join(IMPORT_DIR, `${name}-spec.json`);
  if (!existsSync(specPath)) return null;
  return JSON.parse(readFileSync(specPath, 'utf-8')) as DesignSpecV2;
}

// Check if specs exist (generated by import-integration.test.ts)
const HAS_SPECS = existsSync(join(IMPORT_DIR, 'dashboard-spec.json'));

const describeWithSpecs = HAS_SPECS ? describe : describe.skip;

describeWithSpecs('Render & Compare — DesignSpec V2 screenshots', () => {
  jest.setTimeout(60_000);

  const tokens = buildRendererTokens();

  beforeAll(() => {
    if (!existsSync(SCREENSHOT_DIR)) mkdirSync(SCREENSHOT_DIR, { recursive: true });
  });

  it('renders dashboard spec to a screenshot', async () => {
    const spec = loadSpec('dashboard');
    expect(spec).not.toBeNull();

    const { screenshot, html } = await screenshotDesignSpec(
      spec!,
      tokens,
      V2_BUILTIN_CATALOG,
      { width: 1440, outputPath: join(SCREENSHOT_DIR, 'dashboard-rendered.png') },
    );

    expect(screenshot.length).toBeGreaterThan(1000); // Non-trivial PNG
    expect(html.length).toBeGreaterThan(100);

    // Save HTML for debugging
    writeFileSync(join(SCREENSHOT_DIR, 'dashboard-rendered.html'), html);

    console.log(`Dashboard rendered: ${(screenshot.length / 1024).toFixed(0)}KB PNG, ${(html.length / 1024).toFixed(0)}KB HTML`);
  });

  it('renders settings spec to a screenshot', async () => {
    const spec = loadSpec('settings');
    expect(spec).not.toBeNull();

    const { screenshot, html } = await screenshotDesignSpec(
      spec!,
      tokens,
      V2_BUILTIN_CATALOG,
      { width: 1440, outputPath: join(SCREENSHOT_DIR, 'settings-rendered.png') },
    );

    expect(screenshot.length).toBeGreaterThan(1000);
    writeFileSync(join(SCREENSHOT_DIR, 'settings-rendered.html'), html);
    console.log(`Settings rendered: ${(screenshot.length / 1024).toFixed(0)}KB PNG, ${(html.length / 1024).toFixed(0)}KB HTML`);
  });

  it('renders users spec to a screenshot', async () => {
    const spec = loadSpec('users');
    expect(spec).not.toBeNull();

    const { screenshot, html } = await screenshotDesignSpec(
      spec!,
      tokens,
      V2_BUILTIN_CATALOG,
      { width: 1440, outputPath: join(SCREENSHOT_DIR, 'users-rendered.png') },
    );

    expect(screenshot.length).toBeGreaterThan(1000);
    writeFileSync(join(SCREENSHOT_DIR, 'users-rendered.html'), html);
    console.log(`Users rendered: ${(screenshot.length / 1024).toFixed(0)}KB PNG, ${(html.length / 1024).toFixed(0)}KB HTML`);
  });
});
