/** Write the generated spec to YAML files. */
function writeSpecFiles(
  rootDir: string,
  spec: GeneratedAppSpec,
  fileSystem: FileSystem,
): void {
  const specDir = path.join(rootDir, 'agentforge', 'spec');
  fileSystem.mkdir(specDir);

  // Write pages.yaml
  const pagesData = {
    version: '1.0',
    pages: spec.pages.map((p) => ({
      id: p.id,
      name: p.name,
      description: p.description,
      route: p.route,
      status: 'approved',
      components: [...p.components],
      data_sources: [...p.data_sources],
    })),
  };
  writeYaml(path.join(specDir, 'pages.yaml'), pagesData, fileSystem);

  // Write models.yaml
  const modelsData = {
    version: '1.0',
    models: spec.models.map((m) => ({
      id: m.id,
      name: m.name,
      fields: m.fields.map((f) => ({ ...f })),
      db_table: m.db_table,
    })),
  };
  writeYaml(path.join(specDir, 'models.yaml'), modelsData, fileSystem);

  // Write api.yaml
  const apiData = {
    version: '1.0',
    base_url: '/api',
    endpoints: spec.endpoints.map((e) => ({
      id: e.id,
      method: e.method,
      path: e.path,
      description: e.description,
      query_params: e.query_params.map((q) => ({ ...q })),
      response: { ...e.response },
      auth: e.auth,
      status: 'planned',
    })),
  };
  writeYaml(path.join(specDir, 'api.yaml'), apiData, fileSystem);
}

