/** Check if a hex color is light (for choosing contrast text). */
function isLight(hex: string): boolean {
  const c = hex.replace('#', '');
  const r = parseInt(c.substring(0, 2), 16);
  const g = parseInt(c.substring(2, 4), 16);
  const b = parseInt(c.substring(4, 6), 16);
  return (r * 299 + g * 587 + b * 114) / 1000 > 128;
}

/** Open a URL in the default browser. */
export function openInBrowser(url: string): Promise<boolean> {
  return new Promise((resolve) => {
    const cmd = process.platform === 'darwin' ? 'open'
      : process.platform === 'win32' ? 'start'
      : 'xdg-open';
    exec(`${cmd} "${url}"`, (err) => {
      resolve(!err);
    });
  });
}

/** Print a CLI summary table when browser can't open. */
function printCliSummary(
  options: DesignOption[],
  output: NodeJS.WritableStream,
): void {
  options.forEach((opt, i) => {
    const colors = Object.values(opt.colors.primitive).join(', ');
    output.write(infoMsg(`\n  Option ${i + 1}: ${opt.label}\n`));
    output.write(infoMsg(`    ${opt.vibe}\n`));
    output.write(infoMsg(`    Colors: ${colors}\n`));
    output.write(infoMsg(`    Fonts: ${opt.fonts.display} + ${opt.fonts.body}\n`));
    output.write(infoMsg(`    Tone: ${opt.brand.tone}\n`));
  });
}

/** Prompt for a single line using a short-lived readline. */
export function promptOnce(
  input: NodeJS.ReadableStream,
  output: NodeJS.WritableStream,
  question: string,
): Promise<string> {
  const rl = readline.createInterface({ input, output, terminal: false });
  return new Promise((resolve) => {
    rl.question(question, (answer) => {
      rl.close();
      resolve(answer.trim());
    });
  });
}

/** Options for customizing generateDesignOptions behavior (e.g. in tests). */
export interface GenerateDesignOptionsConfig {
  /** Override browser opener. Return true if browser opened. */
  readonly openBrowser?: (url: string) => Promise<boolean>;
}

/**
 * Generate 3 design system options (LLM or fallback), show preview, let user pick.
 */
export async function generateDesignOptions(
  context: { appName: string; description: string; targetAudience: string; prdContent?: string },
  input?: NodeJS.ReadableStream,
  output?: NodeJS.WritableStream,
  config?: GenerateDesignOptionsConfig,
): Promise<GenerateDesignResult> {
  const out = output ?? process.stdout;
  const inp = input ?? process.stdin;

  let options: DesignOption[];
  let source: 'llm' | 'fallback' = 'fallback';

  // Try LLM generation
  const apiKey = process.env['ANTHROPIC_API_KEY'];
  if (apiKey) {
    out.write(infoMsg('\nGenerating design options with AI...\n'));
    const llmOptions = await tryLLMGeneration(apiKey, context, out);
    if (llmOptions) {
      options = llmOptions;
      source = 'llm';
    } else {
      options = buildFallbackOptions();
    }
  } else {
    options = buildFallbackOptions();
  }

  // Generate HTML preview and open in browser
  const html = generatePreviewHtml(context.appName, options);
  const tmpFile = path.join(os.tmpdir(), `agentforge-design-preview-${Date.now()}.html`);
  fs.writeFileSync(tmpFile, html, 'utf-8');

  const browserFn = config?.openBrowser ?? openInBrowser;
  const browserOpened = await browserFn(`file://${tmpFile}`);
  if (browserOpened) {
    out.write(infoMsg('\nDesign preview opened in your browser.\n'));
  } else {
    out.write(warnMsg('\nCould not open browser. Here are the options:\n'));
    printCliSummary(options, out);
  }

  // Prompt user to choose — read directly from stream to avoid readline buffering
  // issues that consume data intended for subsequent readline consumers
  let choice: number | undefined;

  while (choice === undefined) {
    const answer = await promptOnce(inp, out, '\nChoose 1, 2, or 3 (or \'r\' to regenerate): ');

    if (answer === 'r' && apiKey) {
      out.write(infoMsg('Regenerating...\n'));
      const llmOptions = await tryLLMGeneration(apiKey, context, out);
      if (llmOptions) {
        options = llmOptions;
        source = 'llm';
        const newHtml = generatePreviewHtml(context.appName, options);
        fs.writeFileSync(tmpFile, newHtml, 'utf-8');
        const opened = await browserFn(`file://${tmpFile}`);
        if (!opened) {
          printCliSummary(options, out);
        } else {
          out.write(infoMsg('Preview refreshed in browser.\n'));
        }
      } else {
        out.write(warnMsg('Regeneration failed. Keeping current options.\n'));
      }
      continue;
    }

    const num = parseInt(answer, 10);
    if (num >= 1 && num <= options.length) {
      choice = num;
    } else {
      out.write(warnMsg('Please enter 1, 2, or 3.\n'));
    }
  }

  // Clean up temp file
  try {
    fs.unlinkSync(tmpFile);
  } catch {
    // ignore cleanup errors
  }

  const selected = options[choice - 1];
  const tokens = optionToTokens(selected);
  const brand = optionToBrand(selected, context.targetAudience);

  return { tokens, brand, source };
}

/** Attempt LLM generation, return null on failure. */
async function tryLLMGeneration(
  apiKey: string,
  context: { appName: string; description: string; targetAudience: string; prdContent?: string },
  output: NodeJS.WritableStream,
): Promise<DesignOption[] | null> {
  let provider: LLMProvider;
  try {
    provider = createClaudeProvider('claude-sonnet-4', { apiKey });
  } catch {
    output.write(warnMsg('Failed to create LLM provider, using defaults.\n'));
    return null;
  }

  const systemPrompt = buildSystemPrompt();
  const userPrompt = buildUserPrompt(context);

  for (let attempt = 0; attempt < 2; attempt++) {
    try {
      const result = await provider.complete(
        {
          system: systemPrompt,
          messages: [
            { role: 'user', content: userPrompt },
            ...(attempt > 0
              ? [{ role: 'user' as const, content: 'Your previous response was not valid JSON. Please respond with ONLY the JSON object, no markdown.' }]
              : []),
          ],
        },
        { model: 'claude-sonnet-4', maxTokens: 4096, temperature: 0.8 },
      );

      if (!result.ok) {
        output.write(warnMsg(`LLM request failed: ${JSON.stringify(result.error)}\n`));
        return null;
      }

      const options = parseLLMResponse(result.value.content);
      if (options.length < 2) {
        if (attempt === 0) continue; // retry
        output.write(warnMsg('LLM returned insufficient valid options, using defaults.\n'));
        return null;
      }

      // Validate each option by converting to tokens/brand
      const validOptions = options.filter((opt) => {
        const tokens = optionToTokens(opt);
        const brand = optionToBrand(opt, context.targetAudience);
        const tokensValid = validateDesignTokens(tokens);
        const brandValid = validateBrandSpec(brand);
        return tokensValid.ok && brandValid.ok;
      });

      if (validOptions.length < 2) {
        if (attempt === 0) continue;
        output.write(warnMsg('LLM options failed validation, using defaults.\n'));
        return null;
      }

      return validOptions.slice(0, 3);
    } catch (err) {
      if (attempt === 0) continue;
      output.write(warnMsg(`LLM error: ${err instanceof Error ? err.message : String(err)}\n`));
      return null;
    }
  }

  return null;
}
