/** A single design option generated by the LLM. */
export interface DesignOption {
  readonly label: string;
  readonly vibe: string;
  readonly colors: {
    readonly primitive: Record<string, string>;
    readonly semantic: Record<string, string> & {
      readonly 'background-primary': string;
      readonly 'text-primary': string;
      readonly 'cta-primary': string;
      readonly error: string;
    };
  };
  readonly fonts: {
    readonly display: string;
    readonly body: string;
  };
  readonly brand: {
    readonly tone: string;
    readonly illustrationDirection: string;
    readonly illustrationDescription: string;
    readonly motionFeel: string;
  };
  readonly elevation?: {
    readonly levels: readonly {
      readonly level: number;
      readonly shadow: string;
      readonly description: string;
    }[];
  };
  readonly typography_scale?: readonly TypographyScaleEntry[];
  readonly spacing?: SpacingSpec;
  readonly borders?: BorderSpec;
  readonly touch_targets?: TouchTargetSpec;
  readonly layout?: LayoutSpec;
  readonly z_index?: ZIndexSpec;
  readonly opacity?: OpacitySpec;
  readonly motion?: MotionSpec;
  readonly state?: StateTokensSpec;
  readonly border_styles?: BorderWidthSpec;
  readonly text_extras?: TextExtrasSpec;
  readonly preview?: PreviewData;
}

