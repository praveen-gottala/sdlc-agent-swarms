/** Build the 3 fallback options from existing archetypes. */
function buildFallbackOptions(): DesignOption[] {
  const archetypes: { key: DesignArchetype; label: string; vibe: string }[] = [
    { key: 'warm', label: 'Warm & Inviting', vibe: 'Cream, teal, rounded, friendly — approachable and playful' },
    { key: 'professional', label: 'Clean & Professional', vibe: 'White, slate, blue accent — structured and trustworthy' },
    { key: 'bold', label: 'Bold & Modern', vibe: 'Dark, violet, lime — geometric and cutting-edge' },
  ];

  return archetypes.map(({ key, label, vibe }) => {
    const tokens = buildDesignTokensSpec(key);
    const semantic = tokens.colors.semantic as unknown as DesignOption['colors']['semantic'];
    return {
      label,
      vibe,
      colors: { primitive: tokens.colors.primitive as Record<string, string>, semantic },
      fonts: tokens.typography.font_families as { display: string; body: string },
      brand: {
        tone: key === 'warm' ? 'playful-warm' : key === 'professional' ? 'professional-clean' : 'bold-modern',
        illustrationDirection: 'minimal',
        illustrationDescription: 'Clean illustrations with accent color highlights',
        motionFeel: 'snappy',
      },
      elevation: tokens.elevation,
    };
  });
}

/** Resolve a semantic color to its hex value. */
function resolveColor(semantic: string, primitive: Record<string, string>): string {
  // If semantic value starts with # or rgba, it's already resolved
  if (semantic.startsWith('#') || semantic.startsWith('rgba')) return semantic;
  return primitive[semantic] ?? '#888888';
}

/** Apply opacity to a hex color by appending alpha hex digits. */
function hexWithOpacity(hex: string, opacity: number): string {
  if (hex.startsWith('rgba')) return hex;
  const alpha = Math.round(opacity * 255).toString(16).padStart(2, '0');
  return `${hex.slice(0, 7)}${alpha}`;
}

