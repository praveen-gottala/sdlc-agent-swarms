/** A page definition generated by the LLM. */
export interface GeneratedPage {
  readonly id: string;
  readonly name: string;
  readonly description: string;
  readonly route: string;
  readonly components: readonly string[];
  readonly data_sources: readonly string[];
}

/** A model definition generated by the LLM. */
export interface GeneratedModel {
  readonly id: string;
  readonly name: string;
  readonly fields: readonly {
    readonly name: string;
    readonly type: string;
    readonly nullable?: boolean;
  }[];
  readonly db_table: string;
}

/** An API endpoint generated by the LLM. */
export interface GeneratedEndpoint {
  readonly id: string;
  readonly method: string;
  readonly path: string;
  readonly description: string;
  readonly query_params: readonly { readonly name: string; readonly type: string }[];
  readonly response: { readonly type: string; readonly schema_ref: string };
  readonly auth: string;
}

/** Complete app spec generated by the LLM. */
export interface GeneratedAppSpec {
  readonly pages: readonly GeneratedPage[];
  readonly models: readonly GeneratedModel[];
  readonly endpoints: readonly GeneratedEndpoint[];
}

/** Result of the design:generate flow. */
export interface DesignGenerateResult {
  readonly spec: GeneratedAppSpec;
  readonly source: 'llm' | 'fallback';
}

/** Options for customizing behavior (e.g. in tests). */
export interface DesignGenerateConfig {
  readonly openBrowser?: (url: string) => Promise<boolean>;
  readonly designOptionsConfig?: GenerateDesignOptionsConfig;
}

