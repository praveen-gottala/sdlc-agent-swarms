/** Build the user prompt with full app context. */
function buildUserPrompt(context: {
  readonly appName: string;
  readonly description: string;
  readonly tokens: DesignTokensSpec;
  readonly brand: BrandSpec;
  readonly prdContent?: string;
}): string {
  const colorNames = Object.keys(context.tokens.colors.primitive).join(', ');
  const fonts = `${context.tokens.typography.font_families.display} (headings) + ${context.tokens.typography.font_families.body} (body)`;

  const appContext = context.prdContent
    ? `App: ${context.appName}\n\nPRD:\n${context.prdContent}`
    : `App: ${context.appName}\nDescription: ${context.description || 'A web application'}`;

  return `Generate a complete app specification for:

${appContext}

Design system context:
- Brand tone: ${context.brand.identity.tone}
- Target audience: ${context.brand.identity.audience}
- Color palette: ${colorNames}
- Typography: ${fonts}
- WCAG level: ${context.brand.accessibility.wcag_level}

Generate all pages, data models, and API endpoints needed for this app.`;
}

