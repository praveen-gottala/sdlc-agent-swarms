const BROWNFIELD_APP = join(MONOREPO_ROOT, 'agentforge-brownfield-app');
const IMPORT_DIR = join(BROWNFIELD_APP, '.agentforge', 'import');
const SCREENSHOT_DIR = join(IMPORT_DIR, 'screenshots');

