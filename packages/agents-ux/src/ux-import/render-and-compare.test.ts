/**
 * Render-and-compare test: renders generated DesignSpec V2 JSON files
 * using the browser renderer (real shadcn components), takes screenshots,
 * and compares against screenshots of the actual running app.
 *
 * This validates that the source→DesignSpec pipeline produces specs
 * that render to something visually recognizable.
 *
 * Requires: generated spec files from import-integration.test.ts
 * Requires: brownfield app running on localhost:3001 (or captures will be skipped)
 */

import { join } from 'node:path';
import { readFileSync, existsSync, mkdirSync, writeFileSync } from 'node:fs';
import {
  screenshotDesignSpec,
} from '@agentforge/designspec-renderer';
import { V2_BUILTIN_CATALOG } from '../../../designspec-renderer/src/__fixtures__/catalog-entries.js';
import type { DesignSpecV2, RendererTokens } from '@agentforge/designspec-renderer';

