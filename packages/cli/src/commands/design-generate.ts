/** Parse and validate LLM response into GeneratedAppSpec. */
export function parseAppSpecResponse(raw: string): GeneratedAppSpec | null {
  try {
    const cleaned = raw.replace(/^```(?:json)?\s*/m, '').replace(/\s*```\s*$/m, '').trim();
    const parsed = JSON.parse(cleaned) as GeneratedAppSpec;

    if (!Array.isArray(parsed.pages) || parsed.pages.length === 0) return null;
    if (!Array.isArray(parsed.models) || parsed.models.length === 0) return null;
    if (!Array.isArray(parsed.endpoints) || parsed.endpoints.length === 0) return null;

    // Validate each page has required fields
    const validPages = parsed.pages.filter(
      (p) => p.id && p.name && p.description && p.route && Array.isArray(p.components),
    );
    if (validPages.length === 0) return null;

    // Validate each model has required fields
    const validModels = parsed.models.filter(
      (m) => m.id && m.name && Array.isArray(m.fields) && m.fields.length > 0 && m.db_table,
    );
    if (validModels.length === 0) return null;

    // Validate each endpoint has required fields
    const validEndpoints = parsed.endpoints.filter(
      (e) => e.id && e.method && e.path && e.response,
    );
    if (validEndpoints.length === 0) return null;

    return {
      pages: validPages,
      models: validModels,
      endpoints: validEndpoints,
    };
  } catch {
    return null;
  }
}

